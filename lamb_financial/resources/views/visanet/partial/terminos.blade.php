        <h5 class="centrado">POLÍTICA DE LA PRIVACIDAD DE LA INFORMACIÓN PERSONAL</h5>


        <div align="left" style="text-align: justify;">

            <p>La actual Política de Privacidad refiere todos los términos y condiciones aplicables al uso de nuestros contenidos y servicios disponibles bajo los dominios www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe, por parte de la Universidad Peruana Unión.</p>

            <h6>COMPROMISO DE PRIVACIDAD</h6>
            <p>Para la Universidad Peruana Unión es muy importante la privacidad de la información de sus usuarios. Compartimos el compromiso de proteger su información personal y financiera dándole un tratamiento confidencial. Como proveedores de servicios que involucran información personal - a veces muy sensible - la confidencialidad ha sido, es y continuará siendo una prioridad para nuestra Universidad. </p>

            <h6>TÉRMINOS Y CONDICIONES GENERALES SOBRE LA PRIVACIDAD DE LA INFORMACIÓN</h6>
            <p>A continuación se describen la política de protección de datos de carácter personal para que el Usuario determine libre y voluntariamente si desea facilitar a la Universidad Peruana Unión los Datos Personales que se le pueda requerir o que se pueda obtener del Usuario con ocasión de la suscripción en línea en los servicios ofrecidos por la Universidad a través de sus portales www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe.</p>
            <p>La Universidad Peruana Unión se reserva el derecho de modificar la presente política para adaptarla a novedades legislativas o jurisprudenciales así como prácticas de la industria. En dichos supuestos, la Universidad anunciará en ésta página y/o vía e-mail los cambios introducidos con razonable antelación a su puesta en práctica.</p>
            <p>Ciertos servicios prestados en  www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe pueden contener condiciones particulares con previsiones específicas en materia de protección de Datos Personales.</p>
            <p>Los Datos Personales recogidos serán objeto de tratamiento automatizado e incorporados a los correspondientes ficheros automatizados por la Universidad Peruana Unión, siendo esta Institución titular y responsable del archivo en Perú, país en el que se alojan y tratan los datos personales.</p>
            <p>La Universidad Peruana Unión les proporcionará a los Usuarios los recursos técnicos adecuados para que, con carácter previo, puedan acceder a este aviso sobre la Política de Privacidad o a cualquier otra información relevante y puedan prestar su consentimiento a fin de que la Universidad proceda al tratamiento automatizado de sus Datos Personales. </p>   
            <p>Salvo en los campos en que se indique lo contrario, las respuestas a las preguntas sobre Datos Personales son voluntarias, sin que la falta de contestación implique una merma en la calidad o cantidad de los servicios correspondientes, a menos que se indique lo contrario.</p>
            <p>El Usuario garantiza que los Datos Personales facilitados a la Universidad Peruana Unión son veraces y se hace responsable de comunicar a ésta cualquier modificación en los mismos.</p>
            <p>La recopilación y tratamiento automatizado de los Datos Personales, recabados a través de www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe tienen como finalidad:</p>

            <ol>
                <li>El mantenimiento de la relación establecida con la Universidad Peruana Unión.</li>
                <li>La adecuación de dichos servicios a las preferencias y gustos de los usuarios.</li>
                <li>El estudio de la utilización de los servicios por parte de los usuarios.</li>
                <li>El diseño de nuevos servicios relacionados con dichos servicios.</li>
                <li>El envío de actualizaciones de los servicios.</li>
                <li>El envío, por medios tradicionales y electrónicos, de información técnica y operativa acerca de sus pagos de pensiones o donaciones actualmente y en el futuro.</li>
                <li>La finalidad de la recopilación y tratamiento automatizado de los Datos Personales incluye igualmente el envío de formularios de encuestas, que el Usuario no queda obligado a contestar.</li>
            </ol>

            <p>La Universidad Peruana Unión ha adoptado los niveles de seguridad de protección de los Datos Personales legalmente requeridos y procura instalar aquellos otros medios y medidas técnicas adicionales para evitar la pérdida, mal uso, alteración, acceso no autorizado y robo de los Datos Personales facilitados a la Universidad.</p>
            <p>Los portales web www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe no utilizan cookies ni alguna otra forma de almacenamiento de información en el computador del usuario. </p>
            <p>La Universidad Peruana Unión podrá almacenar y hacer uso de toda la información suministrada por el Usuario dentro de www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe sin restricción alguna, salvo las proporcionadas a entidades financieras al momento de efectuar una transacción de pago en línea. </p>
            <p>Las Empresas Financieras que intervienen en la operación y ejecución de los pagos de las pensiones y donaciones pueden tener Políticas de Privacidad diferentes a las de la Universidad Peruana Unión por lo que no se aplica lo expuesto en el presente documento al tratamiento de los datos proporcionados por el usuario a dichas Empresas Financieras.</p>

