        <h5 class="centrado">POLÍTICA DEL USO DEL SISTEMA</h5>

        <div align="left" style="text-align: justify;">
            <h6>TERMINOS Y CONDICIONES DEL USO DEL SISTEMA</h6>
            <p>Este Acuerdo establece los términos y condiciones que se aplican al uso del sistema de pago de las pensiones y donaciones relacionados en adelante con nuestro sistema; todos son propiedad de la Universidad Peruana Unión, así mismo, el sistema tiene la total  administración y manejo de éstos.</p>
            <p>Este Acuerdo establece derechos y obligaciones a los usuarios, por lo cual debe ser leído cuidadosa y detalladamente, antes de hacer uso del sistema.</p>
            <p>En la página de www.upeu.edu.pe, cooperacion.upeu.edu.pe y/o pagos.upeu.edu.pe, deberá oprimir el botón de realizar la donación o pago de pensión y luego oprimir el botón de proceder a pagar con VISA, según sea la necesidad del usuario y rellenar la información concerniente al usuario tanto para la universidad como para visa.  Al efectuar los pasos anteriores, se estará celebrando legalmente un contrato  que vincula al usuario con la Universidad Peruana Unión.  Lo anterior es debido al uso del sistema, y el usuario debe manifestar que entiende y acepta los términos y condiciones de uso de este acuerdo.</p>
            <ol>
                <li>
                    <h6>Descripción del Sistema para los usuarios:</h6>
                    <p>La Universidad Peruana Unión, mediante sus páginas www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe, brinda un sistema que permite efectuar pagos de pensiones de los alumnos y donaciones utilizando el correo electrónico como medio de notificación. Los usuarios efectúan sus transacciones a través del sitio de los sitios www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe.
                        El Sistema permite realizar transacciones con tarjetas débito y crédito.
                    </p>
                    <p>
                        Los servicios prestados a través del sistema, podrán ser modificados y/o ampliados por la Universidad, situación que será notificada a los usuarios con  anterioridad.
                    </p>
                </li>
                <li>
                    <h6>Condiciones de ingreso al Sistema:</h6>
                    <p>
                        El sistema permite transacciones multimoneda. Pero siempre la transacción final será realizada en nuevos soles; es decir los saldos de las cuentas siempre se verán reflejados en nuevos soles.
                    </p>
                </li>
                <li>
                    <h6>Modo de uso del Sistema.</h6>
                    <p>
                        Para utilizar el Sistema, el Usuario debe ingresar al sitio a través de los dominios www.upeu.edu.pe, cooperacion.upeu.edu.pe y pagos.upeu.edu.pe, u otros dominios debidamente autorizados, que la Universidad Peruana Unión  informará. Una vez adentro, éste podrá realizar las operaciones, como el pago de pensiones y donaciones.
                    </p>
                </li>
                <li>
                    <h6>Tarifas</h6>
                    <p>
                        El sistema de la Universidad Peruana Unión que brinda transacciones online es completamente gratuito y los cargos a los usuarios solo serán en caso de que el banco de origen imponga comisiones con conocimiento y aceptación del usuario.
                    </p>
                </li>
                <li>
                    <h6>Facultades generales</h6>
                    <ol type="a">
                        <li>
                            <p>
                                Uso indebido del Sistema. Si Universidad Peruana Unión  sospecha uso indebido del sistema, incluyendo intento de fraude, actividades de "hacking", o intento de violación de los sistemas de seguridad, la Universidad Peruana Unión podrá iniciar las acciones legales correspondientes.
                            </p>
                        </li>
                        <li>
                            <p>
                                Consulta de datos en la SBS: el usuario autoriza expresamente con la aceptación de presente a la Universidad Peruana Unión para consultar la información en la Superintendencia de Banca y Seguros, en el área de inteligencia financiera y en otros organismos competentes, siempre y cuando la universidad estime conveniente de acuerdo a sus políticas de donaciones.
                            </p>
                        </li>
                    </ol>
                </li>
                <li>
                    <h6>Procesamiento de transacciones.</h6>
                    <p>
                        La Universidad Peruana Unión a través de su sistema  intentará que los abonos por medio de transacciones electrónicas desde y hacia las cuentas bancarias de la universidad se procesen en el menor tiempo posible. Sin embargo, debido a factores fuera del control de la Universidad, no es posible para la Universidad  garantizar el plazo exacto para el destino de los fondos del usuario. La Universidad  no se responsabiliza por daños económicos sufridos por el usuario debido a atrasos o problemas en las transacciones.
                    </p>
                </li>
                <li>
                    <h6>Política de devoluciones</h6>
                    <ol>
