                        <li>Condiciones. La Universidad no realizará retiros justificados, devoluciones, congelamientos o acreditaciones de fondos salvo que el servicio no se preste por causas imputables a la Institución o el donante manifieste el cambio del monto de su transacción.</li>
                        <li>Tiempo. Si el usuario realizó el pago de pensiones y no podrá asistir, deberá informar de esa situación con al menos quince días hábiles de anticipación al inicio de clases. De no cumplir con el plazo establecido la Universidad Peruana Unión no devolverá ni acreditará suma alguna al usuario.</li>
                        <li>Procedimiento. El usuario deberá escribir un correo explicativo dirigido para el caso de alumnos a la Gerencia Financiera al correo gerencia.financiera@upeu.edu.pe y para el caso de donantes al correo direcciondecooperacion@upeu.edu.pe.</li>
                        <li>Asignación. Los alumnos y donantes de la Universidad Peruana Unión no pueden transferir ni asignar los derechos u obligaciones que tengan por el uso de este sistema.</li>
                        <li>Inactividad. El acceso a la información se pierde al inicio de cada periodo lectivo, si el alumno no ha cancelado la matrícula no podrá visualizar sus datos.</li>
